package Exe3;

public class item {
	
	Sring item, autor, local, data, descricao;

	public Sring getItem() {
		return item;
	}

	public void setItem(Sring item) {
		this.item = item;
	}

	public Sring getAutor() {
		return autor;
	}

	public void setAutor(Sring autor) {
		this.autor = autor;
	}

	public Sring getLocal() {
		return local;
	}

	public void setLocal(Sring local) {
		this.local = local;
	}

	public Sring getData() {
		return data;
	}

	public void setData(Sring data) {
		this.data = data;
	}

	public Sring getDescricao() {
		return descricao;
	}

	public void setDescricao(Sring descricao) {
		this.descricao = descricao;
	}
	
	

}
